"""Доменные исключения приложения.

Используются в слоях usecase и services для сигнализации об ошибках
без зависимости от FastAPI. Роутеры перехватывают эти исключения
и преобразуют их в соответствующие HTTP-ответы.
"""


class AppError(Exception):
    """Базовое исключение приложения.

    Все доменные ошибки наследуются от этого класса.
    """


class ConflictError(AppError):
    """Конфликт данных.

    Например: попытка зарегистрировать email, который уже существует.
    """


class UnauthorizedError(AppError):
    """Ошибка аутентификации.

    Например: неверный email или пароль при входе.
    """


class ForbiddenError(AppError):
    """Ошибка авторизации.

    Например: попытка обратиться к ресурсу без необходимых прав.
    """


class NotFoundError(AppError):
    """Объект не найден.

    Например: запрос профиля пользователя с несуществующим ID.
    """


class ExternalServiceError(AppError):
    """Ошибка внешнего сервиса.

    Например: OpenRouter вернул код ошибки или недоступен.
    """
